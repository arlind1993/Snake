package com.company;

public class Cell {

    int pX;
    int pY;

    Cell(int pX, int pY){
        this.pX=pX;
        this.pY=pY;
    }

    public int getpX() {
        return pX;
    }

    public int getpY() {
        return pY;
    }

    public void setpX(int pX) {
        this.pX = pX;
    }

    public void setpY(int pY) {
        this.pY = pY;
    }
}
